int iDes;
void
armControl (bool bSpeedBoost, bool bBtnUp, bool bBtnDown) {
	int iOutput;
	if (bBtnUp || bBtnDown) {
		iOutput = bSpeedBoost ? (bBtnUp - bBtnDown) * 127 : (bBtnUp - bBtnDown) * 60;
		iDes = (SensorValue[I2C_1] + -SensorValue[I2C_2]) / 2;
	} else {
		iOutput = iArmPID(iDes);
	}
	arm (iOutput);
}

